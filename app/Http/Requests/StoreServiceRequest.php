        return [
            'title' => 'required',
            'description' => 'required',
            'font_awesome' => 'required'
        ];
    }

    /**
     * Get custom attributes for validator errors.
     *
     * @return array
     */
    public function attributes()
    {
        return [
            'font_awesome' => "آیکن",
        ];
    }
}
